package me.Techtony96.FinalProject.Buttons.types;

public class BaffleButton {
	//Super class to all buttons
	private double x,y;
	
	//Store input
	public BaffleButton(double x, double y){
		this.x = x;
		this.y = y;
	}
	
	//get variables
	public double getX(){
		return x;
	}
	
	//get variables
	public double getY(){
		return y;
	}

	
	

}
